    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int index = tblProductos.getSelectedRow();
        if (index != -1) {
            int id = (int) tblProductos.getValueAt(index, 0);
            Producto producto = pdao.findById(id);
            ProductoModal modal = new ProductoModal(this, rootPaneCheckingEnabled);
            modal.setTitulo("Editar Producto");
            modal.editarProducto(producto);
            modal.setVisible(true);
            producto = modal.getProducto();
            if (producto != null) {
                pdao.update(producto);
                cargarTabla();
                JOptionPane.showMessageDialog(null, "El producto fue editado exitosamente.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un registro de la tabla.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int index = tblProductos.getSelectedRow();
        if (index != -1) {
            int input = JOptionPane.showConfirmDialog(rootPane, "Esta seguro de eliminar el registro seleccionado?", null, JOptionPane.YES_NO_OPTION);
            if (input == 0) {
                int id = (int) tblProductos.getValueAt(index, 0);
                pdao.delete(id);
                cargarTabla();
                JOptionPane.showMessageDialog(null, "El registro fue eliminado.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un registro de la tabla.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

