    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        ProductoModal modal = new ProductoModal(this, rootPaneCheckingEnabled);
        modal.setTitulo("Nuevo Producto");
        modal.setVisible(true);
        Producto producto = modal.getProducto();
        if (producto != null) {
            pdao.save(producto);
            cargarTabla();
            JOptionPane.showMessageDialog(null, "El producto fue adicionado.");
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

