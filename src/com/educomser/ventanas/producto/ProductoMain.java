    private void cargarTabla() {
        String head[] = {"ID", "Nombre", "Unidad Medida", "Precio", "Stock actual", "Stock minimo", "Fecha venc."};
        DefaultTableModel model = new DefaultTableModel(null, head);
        for (Producto p : pdao.findAll()) {
            model.addRow(new Object[]{
                p.getId(),
                p.getNombre(),
                p.getUnidadMedida(),
                p.getPrecio(),
                p.getStockActual(),
                p.getStockMinimo(),
                p.getFechaVencimiento()
            });
        }
        tblProductos.setModel(model);
    }

