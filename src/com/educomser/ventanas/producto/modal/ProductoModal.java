    private void cargarCategorias() {
        cbxCategorias.removeAllItems();
        for (Categoria cat : catDao.findAll()) {
            cbxCategorias.addItem(cat);
        }
    }

    public void editarProducto(Producto producto) {
        this.producto = producto;
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        txtNombre.setText(producto.getNombre());
        txtUnidadMedida.setText(String.valueOf(producto.getUnidadMedida()));
        txtPrecio.setText(String.valueOf(producto.getPrecio()));
        txtStockActual.setText(String.valueOf(producto.getStockActual()));
        txtStockMinimo.setText(String.valueOf(producto.getStockMinimo()));
        txtFechaVencimiento.setText(df.format(producto.getFechaVencimiento()));
        cbxCategorias.getModel().setSelectedItem(catDao.findById(producto.getIdCategoria()));
    }

    private boolean validarFormulario() {
        if (txtNombre.getText().equals("") || txtNombre == null) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor para el campo nombre.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtUnidadMedida.getText().equals("") || txtUnidadMedida == null) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor para el campo de unidad de medida.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtPrecio.getText().equals("") || txtPrecio == null) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor para el campo precio.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtStockActual.getText().equals("") || txtStockActual == null) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor para el campo stock actual.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtStockMinimo.getText().equals("") || txtStockMinimo == null) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor para el campo stock minimo.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtFechaVencimiento.getText().equals("") || txtFechaVencimiento == null) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor para el campo fecha de vencimiento.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
