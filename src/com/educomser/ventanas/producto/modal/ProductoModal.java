    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if (validarFormulario()) {
                SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
                if (producto == null) {
                    producto = new Producto();
                }
                producto.setNombre(txtNombre.getText());
                producto.setUnidadMedida(txtUnidadMedida.getText());
                producto.setPrecio(Double.parseDouble(txtPrecio.getText()));
                producto.setStockActual(Integer.parseInt(txtStockActual.getText()));
                producto.setStockMinimo(Integer.parseInt(txtStockMinimo.getText()));
                producto.setFechaVencimiento(df.parse(txtFechaVencimiento.getText()));
                producto.setIdCategoria(((Categoria) cbxCategorias.getSelectedItem()).getId());
                dispose();
            }
        } catch (ParseException ex) {
            String msg = "Ingrese una fecha con el siguiente formato dd/mm/aaaa, por ejemplo 26/08/2010";
            Logger.getLogger(ProductoModal.class.getName()).log(Level.SEVERE, msg, ex);
            JOptionPane.showMessageDialog(null, msg, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

